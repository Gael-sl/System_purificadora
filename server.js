// Route optimization: use OpenRouteService if API key provided, otherwise greedy fallback
app.post('/api/rutas/optimize', authMiddleware, requireRole(['admin','repartidor']), async (req,res)=>{
  const items = req.body.items || [];
  // items: array of {id, lat, lng}
  // helper: greedy order (used both for fallback and to give directions a reasonable path)
  const orderGreedy = (list)=>{
    const start = {lat:DELIVERY_CENTER.lat,lng:DELIVERY_CENTER.lng};
    const remaining = list.slice();
    const ordered = [];
    let current = start;
    while(remaining.length){
      remaining.sort((a,b)=>{
        const da = distanceKm(current,{lat:a.lat,lng:a.lng});
        const db = distanceKm(current,{lat:b.lat,lng:b.lng});
        return da-db;
      });
      const next = remaining.shift();
      ordered.push(next);
      current = {lat:next.lat,lng:next.lng};
    }
    return ordered;
  };

  const orderedItems = orderGreedy(items);

  if (orderedItems.length === 0) {
    return res.json({route: [], geometry: []});
  }

  if(process.env.ORS_API_KEY && orderedItems.length){
    try{
      // Build coordinates starting from the delivery center so the geometry begins at base
      const coords = [ [DELIVERY_CENTER.lng, DELIVERY_CENTER.lat], ...orderedItems.map(i=>[i.lng,i.lat]) ];
      const body = {coordinates: coords};
      const orsUrl = 'https://api.openrouteservice.org/v2/directions/driving-car/geojson';
      const resp = await axios.post(orsUrl, body, {
        headers:{Authorization: process.env.ORS_API_KEY,'Content-Type':'application/json'}
      });
      const geometry = resp.data?.features?.[0]?.geometry?.coordinates || [];
      return res.json({route: orderedItems, geometry});
    }catch(e){
      const status = e.response?.status;
      if (status === 404) {
        try {
          const fallbackUrl = 'https://api.openrouteservice.org/v2/directions/driving-car';
          const resp = await axios.post(fallbackUrl, body, {
            headers:{Authorization: process.env.ORS_API_KEY,'Content-Type':'application/json'},
            params:{format:'geojson'}
          });
          const geometry = resp.data?.features?.[0]?.geometry?.coordinates || [];
          return res.json({route: orderedItems, geometry});
        } catch (inner) {
          console.error('ORS fallback error:', inner.response?.status || inner.message);
        }
      } else {
        console.error('ORS directions error:', status || e.message);
      }
      // fall through to greedy polyline without streets
    }
  }

  // OSRM fallback for street-following geometry (no API key)
  try{
    const coords = [ [DELIVERY_CENTER.lng, DELIVERY_CENTER.lat], ...orderedItems.map(i=>[i.lng,i.lat]) ];
    const coordString = coords.map(c => `${c[0]},${c[1]}`).join(';');
    const osrmUrl = `https://router.project-osrm.org/route/v1/driving/${coordString}`;
    const resp = await axios.get(osrmUrl, { params: { overview: 'full', geometries: 'geojson' } });
    const geometry = resp.data?.routes?.[0]?.geometry?.coordinates || [];
    if (geometry.length) {
      return res.json({route: orderedItems, geometry});
    }
  }catch(e){
    console.error('OSRM directions error:', e.response?.status || e.message);
  }

  // fallback: greedy nearest-neighbor and straight segments
  const route = orderedItems;
  const geometry = [ [DELIVERY_CENTER.lng, DELIVERY_CENTER.lat], ...route.map(i=>[i.lng,i.lat]) ];
  res.json({route, geometry});
});

