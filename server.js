app.post('/api/clientes', async (req,res)=>{
  let { nombre, telefono, direccion, lat, lng } = req.body;

  // Si no hay lat/lng, intentar geocodificar usando Nominatim con soportes de cruce de calles
  if (!lat || !lng) {
    try {
      const dirText = (direccion || '').trim();
      let foundPoint = null;

      // Caso: "Calle Principal entre Calle 1 y Calle 2" -> promediar los dos cruces para caer sobre la calle principal
      const entreMatch = dirText.match(/(.+?)\s+entre\s+(.+?)\s+(?:y|e)\s+(.+)/i);
      if (entreMatch) {
        const base = entreMatch[1].trim();
        const cross1 = entreMatch[2].trim();
        const cross2 = entreMatch[3].trim();
        const p1 = await geocodeIntersection(base, cross1);
        const p2 = await geocodeIntersection(base, cross2);
        if (p1 && p2) {
          foundPoint = { lat: (p1.lat + p2.lat) / 2, lng: (p1.lng + p2.lng) / 2 };
        } else if (p1) {
          foundPoint = p1;
        } else if (p2) {
          foundPoint = p2;
        }
      }

      // Caso: "Calle A y Calle B" o "Calle A & Calle B"
      if (!foundPoint) {
        const yMatch = dirText.match(/(.+?)\s+(?:y|&){1}\s+(.+)/i);
        if (yMatch) {
          const p = await geocodeIntersection(yMatch[1].trim(), yMatch[2].trim());
          if (p) foundPoint = p;
        }
      }

      // Búsqueda directa si no hubo cruces
      if (!foundPoint) {
        const query = encodeURIComponent(`${dirText}, Gabriel Leyva Solano, Sinaloa, Mexico`);
        const geoRes = await axios.get(`https://nominatim.openstreetmap.org/search?format=json&q=${query}&limit=1`, {
          headers: { 'User-Agent': 'SistemaPurificadora/1.0' }
        });
        if (geoRes.data && geoRes.data.length > 0) {
          foundPoint = { lat: parseFloat(geoRes.data[0].lat), lng: parseFloat(geoRes.data[0].lon) };
        }
      }

      if (foundPoint) {
        lat = foundPoint.lat;
        lng = foundPoint.lng;
      } else {
        // Fallback: centro con ligera variación para no encimar
        lat = DELIVERY_CENTER.lat + (Math.random() - 0.5) * 0.01;
        lng = DELIVERY_CENTER.lng + (Math.random() - 0.5) * 0.01;
      }
    } catch (e) {
      console.error("Error geocoding:", e.message);
      lat = DELIVERY_CENTER.lat;
      lng = DELIVERY_CENTER.lng;
    }
  }

  const nuevo = { nombre, telefono, direccion, lat, lng, id: Date.now() };

  if(pgPool){
    const { rows } = await pgPool.query(
      'INSERT INTO clientes (nombre, telefono, direccion, lat, lng) VALUES ($1,$2,$3,$4,$5) RETURNING *',
      [nombre, telefono, direccion, lat, lng]
    );
    if(rows && rows[0]) return res.json(rows[0]);
  }
  clientes.push(nuevo);
  res.json(nuevo);
});

