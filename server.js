async function initDb(){
  if(!pgPool) return;
  await pgPool.query(`
    CREATE TABLE IF NOT EXISTS clientes (
      id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      nombre TEXT NOT NULL,
      telefono TEXT,
      direccion TEXT,
      lat DOUBLE PRECISION,
      lng DOUBLE PRECISION,
      frecuencia INTEGER,
      volumen INTEGER
    );
  `);
  await pgPool.query(`
    CREATE TABLE IF NOT EXISTS pedidos (
      id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      cliente_id INTEGER REFERENCES clientes(id),
      total NUMERIC,
      estado TEXT,
      cantidad INTEGER,
      creado TIMESTAMP DEFAULT NOW(),
      notas TEXT
    );
  `);
  await pgPool.query(`
    CREATE TABLE IF NOT EXISTS ventas (
      id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      cliente_id INTEGER,
      total NUMERIC,
      metodo TEXT,
      creado TIMESTAMP DEFAULT NOW()
    );
  `);
  await pgPool.query(`
    CREATE TABLE IF NOT EXISTS inventario (
      id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      item TEXT,
      stock INTEGER,
      umbral INTEGER,
      precio NUMERIC
    );
  `);

  await pgPool.query(`
    CREATE TABLE IF NOT EXISTS gastos (
      id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      concepto TEXT,
      categoria TEXT,
      monto NUMERIC,
      creado TIMESTAMP DEFAULT NOW()
    );
  `);

  await pgPool.query(`ALTER TABLE inventario ADD COLUMN IF NOT EXISTS precio NUMERIC;`);
  await pgPool.query(`ALTER TABLE inventario ADD COLUMN IF NOT EXISTS umbral INTEGER;`);

  const { rows: cCount } = await pgPool.query('SELECT COUNT(*) AS count FROM clientes');
  if (Number(cCount[0].count) === 0 && clientes.length) {
    for (const c of clientes) {
      await pgPool.query(
        'INSERT INTO clientes (id, nombre, telefono, direccion, lat, lng, frecuencia, volumen) VALUES ($1,$2,$3,$4,$5,$6,$7,$8) ON CONFLICT (id) DO NOTHING',
        [c.id, c.nombre, c.telefono, c.direccion, c.lat, c.lng, c.frecuencia, c.volumen]
      );
    }
  }

  const { rows: pCount } = await pgPool.query('SELECT COUNT(*) AS count FROM pedidos');
  if (Number(pCount[0].count) === 0 && pedidos.length) {
    for (const p of pedidos) {
      await pgPool.query(
        'INSERT INTO pedidos (id, cliente_id, total, estado, cantidad, creado, notas) VALUES ($1,$2,$3,$4,$5,$6,$7) ON CONFLICT (id) DO NOTHING',
        [p.id, p.cliente_id, p.total, p.estado, p.cantidad, p.creado, p.notas]
      );
    }
  }

  const { rows: vCount } = await pgPool.query('SELECT COUNT(*) AS count FROM ventas');
  if (Number(vCount[0].count) === 0 && ventas.length) {
    for (const v of ventas) {
      await pgPool.query(
        'INSERT INTO ventas (id, cliente_id, total, metodo, creado) VALUES ($1,$2,$3,$4,$5) ON CONFLICT (id) DO NOTHING',
        [v.id, v.cliente_id, v.total, v.metodo, v.creado]
      );
    }
  }

  const { rows: iCount } = await pgPool.query('SELECT COUNT(*) AS count FROM inventario');
  if (Number(iCount[0].count) === 0 && inventario.length) {
    for (const i of inventario) {
      await pgPool.query(
        'INSERT INTO inventario (id, item, stock, umbral, precio) VALUES ($1,$2,$3,$4,$5) ON CONFLICT (id) DO NOTHING',
        [i.id, i.item, i.stock, i.umbral, i.precio]
      );
    }
  }

  const { rows: gCount } = await pgPool.query('SELECT COUNT(*) AS count FROM gastos');
  if (Number(gCount[0].count) === 0 && gastos.length) {
    for (const g of gastos) {
      await pgPool.query(
        'INSERT INTO gastos (id, concepto, categoria, monto, creado) VALUES ($1,$2,$3,$4,$5) ON CONFLICT (id) DO NOTHING',
        [g.id, g.concepto, g.categoria, g.monto, g.creado]
      );
    }
  }

  // Ensure identity sequences are ahead of max(id) to avoid duplicate key inserts
  await pgPool.query(
    "SELECT setval(pg_get_serial_sequence('clientes','id'), GREATEST(COALESCE((SELECT MAX(id) FROM clientes), 0), 1), true)"
  );
  await pgPool.query(
    "SELECT setval(pg_get_serial_sequence('pedidos','id'), GREATEST(COALESCE((SELECT MAX(id) FROM pedidos), 0), 1), true)"
  );
  await pgPool.query(
    "SELECT setval(pg_get_serial_sequence('ventas','id'), GREATEST(COALESCE((SELECT MAX(id) FROM ventas), 0), 1), true)"
  );
  await pgPool.query(
    "SELECT setval(pg_get_serial_sequence('inventario','id'), GREATEST(COALESCE((SELECT MAX(id) FROM inventario), 0), 1), true)"
  );
  await pgPool.query(
    "SELECT setval(pg_get_serial_sequence('gastos','id'), GREATEST(COALESCE((SELECT MAX(id) FROM gastos), 0), 1), true)"
  );
}

// Helper: fetch from Postgres if configured, otherwise use in-memory
async function getClientes(zona){
  if(pgPool){
    const { rows } = await pgPool.query('SELECT * FROM clientes');
    if (!zona) return rows;
    return rows.filter(c=> c.lat && c.lng ? distanceKm(DELIVERY_CENTER,{lat:c.lat,lng:c.lng})<=DELIVERY_RADIUS_KM : true);
  }
  if (!zona) return clientes;
  return clientes.filter(c=> c.lat && c.lng ? distanceKm(DELIVERY_CENTER,{lat:c.lat,lng:c.lng})<=DELIVERY_RADIUS_KM : true);
}

async function getPedidos(zona){
  if(pgPool){
    const { rows: pedidosData } = await pgPool.query('SELECT * FROM pedidos');
    const { rows: clientesData } = await pgPool.query('SELECT * FROM clientes');
    const clientesList = clientesData || clientes;
    if (!zona) return pedidosData;
    return pedidosData.filter(p=>{
      if(!p.cliente_id) return true; // Mostrar pedidos sin cliente registrado (eventuales)
      const c = clientesList.find(x=>x.id===p.cliente_id);
      if (!c) return true; 
      if (!c.lat || !c.lng) return true;
      return distanceKm(DELIVERY_CENTER,{lat:c.lat,lng:c.lng})<=DELIVERY_RADIUS_KM;
    });
  }
  if (!zona) return pedidos;
  return pedidos.filter(p=>{
    if(!p.cliente_id) return true; // Mostrar pedidos sin cliente registrado (eventuales)
    const c = clientes.find(x=>x.id===p.cliente_id);
    if (!c) return true;
    if (!c.lat || !c.lng) return true;
    return distanceKm(DELIVERY_CENTER,{lat:c.lat,lng:c.lng})<=DELIVERY_RADIUS_KM;
  });
}

