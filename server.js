// Haversine distance (km)
function distanceKm(a,b){
  const toRad = v=>v*Math.PI/180;
  const R = 6371; // km
  const dLat = toRad(b.lat - a.lat);
  const dLon = toRad(b.lng - a.lng);
  const lat1 = toRad(a.lat);
  const lat2 = toRad(b.lat);
  const sinDLat = Math.sin(dLat/2);
  const sinDLon = Math.sin(dLon/2);
  const aa = sinDLat*sinDLat + sinDLon*sinDLon * Math.cos(lat1)*Math.cos(lat2);
  const c = 2 * Math.atan2(Math.sqrt(aa), Math.sqrt(1-aa));
  return R * c;
}

// Geocode intersection of two streets in Gabriel Leyva Solano
async function geocodeIntersection(streetA, streetB){
  const query = encodeURIComponent(`${streetA} & ${streetB}, Gabriel Leyva Solano, Sinaloa, Mexico`);
  const url = `https://nominatim.openstreetmap.org/search?format=json&q=${query}&limit=1`;
  const resp = await axios.get(url, { headers: { 'User-Agent': 'SistemaPurificadora/1.0' } });
  if (resp.data && resp.data.length > 0) {
    return { lat: parseFloat(resp.data[0].lat), lng: parseFloat(resp.data[0].lon) };
  }
  return null;
}

